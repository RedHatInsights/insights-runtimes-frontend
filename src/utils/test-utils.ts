/**
 * Function that creates a deep copy of a passed instance by creating a string copy and parsing it.
 *
 * Useful for tests because JavaScript is pass by reference, so trying to do manipulation of the
 * test instances will result in altering the base instance values. Furthermore, instance.title
 * for the accordion toggle header is generated by the formatting function, so without working
 * on a copy the base test instance will be updated to have a title attribute.
 * @param instance JvmInstance
 * @returns a deep copy of (and not a reference to) a JvmInstance
 */
export const getCopyOfInstance = (instance: JvmInstance) => {
  return JSON.parse(JSON.stringify(instance));
};
